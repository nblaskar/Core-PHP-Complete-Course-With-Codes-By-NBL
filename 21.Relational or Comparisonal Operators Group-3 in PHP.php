<?php

	
	
	
	
	//Not Equal To(!=)
	$num1=10;
	$num2=20;
	if($num1 != $num2)
		echo "Yes! Number is Not Equal";
	else
		echo "No! Number is Equal";


	//Not Equal To(<>)
	$num1=10;
	$num2=20;
	if($num1 <> $num2)
		echo "Yes! Number is Not Equal";
	else
		echo "No! Number is Equal";


	
	//Not Identical To (!==)
	$num1=10;
	$num2="10";
	if($num1!==$num2)
		echo "Yes! Number is Not Equal";
	else
		echo "No! Number is Equal";

	
	
?>